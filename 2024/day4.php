function checkXmas(
    string $expectedX,
    string $expectedM,
    string $expectedA,
    string $expectedS
): bool {
    if ($expectedX != "X") {
        return false;
    } elseif ($expectedM != "M") {
        return false;
    } elseif ($expectedA != "A") {
        return false;
    } elseif ($expectedS != "S") {
        return false;
    }

    return true;
}

function checkMas(string $expectedM, string $expectedA, string $expectedS): bool
{
