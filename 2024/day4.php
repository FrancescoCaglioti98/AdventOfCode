function firstCheck(array $matrix): int
{
    $counterOccurence = 0;
    foreach ($matrix as $indexRow => $row) {
        foreach ($row as $indexCell => $cell) {
            //Orizzotale?
            if (array_key_exists($indexCell + 3, $row)) {
                $x = $cell;
                $m = $row[$indexCell + 1];
                $a = $row[$indexCell + 2];
                $s = $row[$indexCell + 3];

                if (checkXmas($x, $m, $a, $s)) {
                    $counterOccurence++;
                }
            }

            //Orizzontale reverse?
            if (array_key_exists($indexCell - 3, $row)) {
                $s = $row[$indexCell - 3];
                $a = $row[$indexCell - 2];
                $m = $row[$indexCell - 1];
                $x = $cell;

                if (checkXmas($x, $m, $a, $s)) {
                    $counterOccurence++;
                }
            }

            //Verticale?
            if (array_key_exists($indexRow + 3, $matrix)) {
                $x = $cell;
                $m = $matrix[$indexRow + 1][$indexCell];
                $a = $matrix[$indexRow + 2][$indexCell];
                $s = $matrix[$indexRow + 3][$indexCell];

                if (checkXmas($x, $m, $a, $s)) {
                    $counterOccurence++;
                }
            }
            //Verticale reverse?
            if (array_key_exists($indexRow - 3, $matrix)) {
                $s = $matrix[$indexRow - 3][$indexCell];
                $a = $matrix[$indexRow - 2][$indexCell];
                $m = $matrix[$indexRow - 1][$indexCell];
                $x = $cell;

                if (checkXmas($x, $m, $a, $s)) {
                    $counterOccurence++;
                }
            }

            //Diagonale DX basso?
            if (
                array_key_exists($indexRow + 3, $matrix) &&
                array_key_exists($indexCell + 3, $row)
            ) {
                $x = $cell;
                $m = $matrix[$indexRow + 1][$indexCell + 1];
                $a = $matrix[$indexRow + 2][$indexCell + 2];
                $s = $matrix[$indexRow + 3][$indexCell + 3];

                if (checkXmas($x, $m, $a, $s)) {
                    $counterOccurence++;
                }
            }
            //Diagonale DX basso reverse?
            if (
                array_key_exists($indexRow - 3, $matrix) &&
                array_key_exists($indexCell - 3, $row)
            ) {
                $s = $matrix[$indexRow - 3][$indexCell - 3];
                $a = $matrix[$indexRow - 2][$indexCell - 2];
                $m = $matrix[$indexRow - 1][$indexCell - 1];
                $x = $cell;

                if (checkXmas($x, $m, $a, $s)) {
                    $counterOccurence++;
                }
            }

            //Diagonale DX alto?
            if (
                array_key_exists($indexRow - 3, $matrix) &&
                array_key_exists($indexCell + 3, $row)
            ) {
                $x = $cell;
                $m = $matrix[$indexRow - 1][$indexCell + 1];
                $a = $matrix[$indexRow - 2][$indexCell + 2];
                $s = $matrix[$indexRow - 3][$indexCell + 3];

                if (checkXmas($x, $m, $a, $s)) {
                    $counterOccurence++;
                }
            }
            //Diagonale DX alto reverse?
            if (
                array_key_exists($indexRow + 3, $matrix) &&
                array_key_exists($indexCell - 3, $row)
            ) {
                $s = $matrix[$indexRow + 3][$indexCell - 3];
                $a = $matrix[$indexRow + 2][$indexCell - 2];
                $m = $matrix[$indexRow + 1][$indexCell - 1];
                $x = $cell;

                if (checkXmas($x, $m, $a, $s)) {
                    $counterOccurence++;
                }
            }
        }
    }

    return $counterOccurence;
}

function secondCheck(array $matrix): int
{
    $counterOccurence = 0;

    foreach ($matrix as $indexRow => $row) {
        foreach ($row as $indexCell => $cell) {
            if ($cell !== "A") {
                continue;
            }
            if (!array_key_exists($indexRow + 1, $matrix)) {
                continue;
            } elseif (!array_key_exists($indexRow - 1, $matrix)) {
                continue;
            } elseif (!array_key_exists($indexCell + 1, $row)) {
                continue;
            } elseif (!array_key_exists($indexCell - 1, $row)) {
                continue;
            }

            $a = $cell;

            $hasDiagonal2 = false;

            //Prima Diagonale
            $m = $matrix[$indexRow - 1][$indexCell - 1];
            $s = $matrix[$indexRow + 1][$indexCell + 1];
            $hasDiagonal1 = checkMas($m, $a, $s);
            if (!$hasDiagonal1) {
                $s = $matrix[$indexRow - 1][$indexCell - 1];
                $m = $matrix[$indexRow + 1][$indexCell + 1];
                $hasDiagonal1 = checkMas($m, $a, $s);
            }

            //Seconda Diagonale
            $m = $matrix[$indexRow - 1][$indexCell + 1];
            $s = $matrix[$indexRow + 1][$indexCell - 1];
            $hasDiagonal2 = checkMas($m, $a, $s);
            if (!$hasDiagonal2) {
                $s = $matrix[$indexRow - 1][$indexCell + 1];
                $m = $matrix[$indexRow + 1][$indexCell - 1];
                $hasDiagonal2 = checkMas($m, $a, $s);
            }

            if ($hasDiagonal1 && $hasDiagonal2) {
                $counterOccurence++;
            }
        }
    }

    return $counterOccurence;
}

